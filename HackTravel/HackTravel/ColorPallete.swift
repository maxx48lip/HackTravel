import UIKit

/// Палитра цветов приложения
enum ColorPallete {

	/// Черный - (Основной) цвет фона
	static let trueBlack: UIColor = UIColor(red: 6 / 255, green: 5 / 255, blue: 7 / 255, alpha: 1)

	/// Белый - (Основной) цвет заголовков и кнопок
	static let trueWhite: UIColor = UIColor(red: 251 / 255, green: 251 / 255, blue: 251 / 255, alpha: 1)

	/// Темный Серый - (Основной) цвет текстов и ячеек
	static let trueDarkGray: UIColor = UIColor(red: 73 / 255, green: 85 / 255, blue: 77 / 255, alpha: 1)

	/// Светлый Серый - (Альтернативный) цвет текстов и ячеек
	static let trueLightGray: UIColor = UIColor(red: 165 / 255, green: 165 / 255, blue: 165 / 255, alpha: 1)

	/// Синий - (Основной) цвет для графиков
	static let trueBlue: UIColor = UIColor(red: 41 / 255, green: 127 / 255, blue: 234 / 255, alpha: 1)

	/// Оранжевый - (Основной) цвет для графиков
	static let trueOrange: UIColor = UIColor(red: 234 / 255, green: 115 / 255, blue: 41 / 255, alpha: 1)

	/// Зеленый - (Опциональный) цвет для подписей
	static let trueGreen: UIColor = UIColor(red: 34 / 255, green: 164 / 255, blue: 82 / 255, alpha: 1)

	static let trueRed: UIColor = UIColor(red: 161 / 255, green: 9 / 255, blue: 10 / 255, alpha: 1)

	static let trueDarkRed: UIColor = UIColor(red: 94 / 255, green: 4 / 255, blue: 5 / 255, alpha: 1)
}
